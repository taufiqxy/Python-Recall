########################################################################################################################
# Part II (For)
########################################################################################################################

buahBuahan = ['Kelapa', 'Nangka', 'Langsat', 'Duku', 'Rambutan', 'Durian']
for buah in buahBuahan:
    print(buah)

# another way
for i in range(len(buahBuahan)):
    print(buahBuahan[i])

print()

########################################################################################################################
# Part III (While)
########################################################################################################################

i = 0
while i < 10:
    print(i)
    i += 1

print()

########################################################################################################################
# Part IV (Old Memories)
########################################################################################################################

for i in range(10):
    for j in range(10-i):
        print('*', end='')
    print()
